import Head from "next/head";
import AccessoriesSwiper from "./AccessoriesSwiper";
import Categories from "./Categories";
import Discover from "./Discover";
import LandingSection from "./LandingSection";
import LaptopsSwiper from "./LaptopsSwiper";
import Marketing from "./Marketing";
import SecondSection from "./SecondSection";
import { api } from "~/utils/api";
import type { Product } from "types";

type ProductProps = {
  products: Product[];
};

const Home = ({ products }: ProductProps) => {
  return (
    <>
      <Head>
        <title>BlackBeardt | Home</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <LandingSection products={products} />
        <SecondSection />
        <Categories />
        <Discover />
        <LaptopsSwiper />
        <AccessoriesSwiper />
        <Marketing />
      </main>
    </>
  );
};

export function getStaticProps() {
  const { data } = api.products.getAll.useQuery({});
  const products = data ?? [];

  return {
    props: {
      products,
    },
    // Next.js will attempt to re-generate the page:
    // - When a request comes in
    // - At most once every 10 seconds
    revalidate: 10, // In seconds
  };
}

export default Home;
